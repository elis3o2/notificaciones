    id INT(20) PRIMARY KEY NOT NULL,
    id_estado INT NOT NULL,
    msj_confirmado TINYINT(1) NOT NULL,
    msj_reprogramado TINYINT(1) NOT NULL,
    msj_cancelado TINYINT(1) NOT NULL,
    msj_recordatorio TINYINT(1) NOT NULL,
    id_efe_ser_esp INT NOT NULL,
    fecha DATE NOT NULL,
    hora TIME NOT NULL,
    FOREIGN KEY (id_estado) REFERENCES estado_turno(id),
    FOREIGN KEY (id_efe_ser_esp) REFERENCES efe_ser_esp(id)
