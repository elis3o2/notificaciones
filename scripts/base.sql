    id_estado INT NOT NULL,
    FOREIGN KEY (id_turno) REFERENCES turno(id),
    FOREIGN KEY (id_estado) REFERENCES estado_msj(id),
    FOREIGN KEY (id_plantilla) REFERENCES plantilla(id)
);




